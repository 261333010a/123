package Test_Extends;

public class TestExtends {
    /*
    继承的含义：
    ①被继承的类叫做父类，也叫做超类  super class
        继承的类叫子类  son class
    ②子类是父类的一种
    ③子类继承父类的所有属性和方法，并可以有自己的方法和属性
     */

    /*
    super()
    ①只能出现在子类的构造器中，必须出现在首行
    ②super() 匹配父类空构造器
    ③super("")匹配有参的构造方法
    ④在同一构造器中super()和this()不能同时出现
    ⑤子类空构造方法默认调用父类的空构造方法，如果父类没有空构造方法就会报错
     */

    /*
    this()
        作用：调用本类的构造器
        只能写在构造器的第一行
     */
}
